package at.fhj.msd;

public class Color {

    private int red;
    private int green;
    private int blue;


    public Color(int red, int green, int blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }

    //? Constructor

    public int getRed() {
        return this.red;
    }

    public int getGreen() {
        return this.green;
    }

    public int getGetBlue() {
        return this.blue;
    }
    
}
